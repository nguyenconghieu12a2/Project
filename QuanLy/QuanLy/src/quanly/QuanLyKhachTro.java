    public void traphong(NhaTro nt) {
        int cmndFind = Integer.parseInt(txtCmnd.getText());
        int thangnhan = Integer.parseInt(txtThangNhan.getText());
        int namnhan = Integer.parseInt(txtNamNhan.getText());
        NhaTro ntFind = qlDAO.findbyCMND(cmndFind);
        nt.setThangTra();

        if (ntFind != null) {
            setModel(ntFind);
            int thangtra = Integer.parseInt(txtThangTra.getText());
            int namtra = Integer.parseInt(txtNamTra.getText());
            txtThangTra.setText(String.valueOf(nt.getNamTra()));
            txtNamTra.setText(String.valueOf(nt.getThangTra()));

            int totalPrice;
            int sothang = ((namtra - namnhan) * 12) + (thangtra - thangnhan);
            switch (txtLoaiPhong.getText()) {
                case "a":
                    totalPrice = sothang * 2500000;
                    txtTinhTien.setText(String.valueOf(totalPrice));
                    txtThangTra.setText(String.valueOf(thangtra));
                    txtNamTra.setText(String.valueOf(namtra));
                    break;
                case "A":
                    totalPrice = sothang * 2500000;
                    txtTinhTien.setText(String.valueOf(totalPrice));
                    txtThangTra.setText(String.valueOf(thangtra));
                    txtNamTra.setText(String.valueOf(namtra));
                    break;
                case "b":
                    totalPrice = sothang * 2000000;
                    txtTinhTien.setText(String.valueOf(totalPrice));
                    txtThangTra.setText(String.valueOf(thangtra));
                    txtNamTra.setText(String.valueOf(namtra));
                    break;
                case "B":
                    totalPrice = sothang * 2000000;
                    txtTinhTien.setText(String.valueOf(totalPrice));
                    txtThangTra.setText(String.valueOf(thangtra));
                    txtNamTra.setText(String.valueOf(namtra));
                    break;
                case "c":
                    totalPrice = sothang * 1500000;
                    txtTinhTien.setText(String.valueOf(totalPrice));
                    txtThangTra.setText(String.valueOf(thangtra));
                    txtNamTra.setText(String.valueOf(namtra));
                    break;
                case "C":
                    totalPrice = sothang * 1500000;
                    txtTinhTien.setText(String.valueOf(totalPrice));
                    txtThangTra.setText(String.valueOf(thangtra));
                    txtNamTra.setText(String.valueOf(namtra));
                    break;
                default:
                    JOptionPane.showMessageDialog(this, "Invalid room!!!");
                    break;
            }

        } else {
            JOptionPane.showMessageDialog(this, "Customer not found!!!");
        }
    }

    private void btnTraPhongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTraPhongActionPerformed
        // TODO add your handling code here:
        if (txtTen.getText().isEmpty()
                || txtTuoi.getText().isEmpty()
                || txtQueQuan.getText().isEmpty()
                || txtCmnd.getText().isEmpty()
                || txtThangNhan.getText().isEmpty()
                || txtNamNhan.getText().isEmpty()
                || txtLoaiPhong.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all information!!!");
        } else {
            int cmndFind = Integer.parseInt(txtCmnd.getText());
            NhaTro ntFind = qlDAO.findbyCMND(cmndFind);
            if (ntFind != null) {
                setModel(ntFind);
                traphong(ntFind);
                qlDAO.delete(cmndFind);
            } else {
                JOptionPane.showMessageDialog(this, "Customer not found!!!");
            }
        }
    }//GEN-LAST:event_btnTraPhongActionPerformed

