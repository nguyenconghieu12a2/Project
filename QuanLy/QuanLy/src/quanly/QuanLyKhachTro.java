    public NhaTro getModel() {
        NhaTro nt = new NhaTro();
        nt.setName(txtTen.getText());
        nt.setAge(Integer.parseInt(txtTuoi.getText()));
        nt.setQuequan(txtQueQuan.getText());
        nt.setCmnd(Integer.parseInt(txtCmnd.getText()));
        nt.setThangThueTro(Integer.parseInt(txtThangNhan.getText()));
        nt.setNamThueTro(Integer.parseInt(txtNamNhan.getText()));
        nt.setLoaiPhong(txtLoaiPhong.getText());
        return nt;
    }

    public int checkTuoi() {
        for (int i = 0; i < txtTuoi.getText().length(); i++) {
            if (txtTuoi.getText().charAt(i) >= 'a' && txtTuoi.getText().charAt(i) >= 'z'
                    || txtTuoi.getText().charAt(i) >= 'A' && txtTuoi.getText().charAt(i) >= 'Z') {
                return 0;
            }
        }
        return 1;
    }

